util.AddNetworkString("asa_animasyon")
////////////////////////////////////////////////////////////////////////////
net.Receive( "asa_animasyon", function( len, ply )
////////////////////////////////////////////////////////////////////////////
	local butoncalistir = net.ReadFloat()
	if ply:GetActiveWeapon( ):GetClass( ) == "weapon_hpwr_stick" then
    if (butoncalistir == 1) then
	ply:GetActiveWeapon():SetHoldType("melee")
    end
////////////////////////////////////////////////////////////////////////////
     if (butoncalistir == 2) then
	ply:GetActiveWeapon():SetHoldType("ofansif")
    end
////////////////////////////////////////////////////////////////////////////
    if (butoncalistir == 3) then
	ply:GetActiveWeapon():SetHoldType("saldirgan")
    end
////////////////////////////////////////////////////////////////////////////
     if (butoncalistir == 4) then
	ply:GetActiveWeapon():SetHoldType("defansif")
    end
end
end )
////////////////////////////////////////////////////////////////////////////
MsgC(Color(9,226,175), "[BT Animasyon Sistemi]", Color(255, 255, 255), " animsec.lua yuklendi! \n")
